def format_technique_html(t: dict) -> str:
    # Форматируем в HTML: <b>Название</b>\n<i>зачем</i>\n• шаги\n\n✨ pep
    lines = []
    lines.append(f"<b>{t['title']}</b>")
    lines.append(f"<i>{t['why']}</i>")
    lines.append("")  # пустая строка
    for s in t["steps"]:
        lines.append(f"• {s}")
    lines.append("")  # пустая строка
    lines.append(f"✨ <i>{t['pep']}</i>")
    return "\n".join(lines)

# ---------- ХЕНДЛЕРЫ ----------
async def menu_me_tiazhelo(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    await update.message.reply_text(
        "Что именно ты чувствуешь сейчас?",
        reply_markup=HARD_MENU_KB
    )

async def handle_back(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    await update.message.reply_text(
        "Я рядом. Выбери, что тебе нужно.",
        reply_markup=MAIN_MENU_KB
    )

async def handle_trevoha(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    t = random.choice(TECHNIQUES_TREVOHA)
    await update.message.reply_text(
        format_technique_html(t),
        parse_mode=ParseMode.HTML,
        reply_markup=ANXIETY_FLOW_KB
    )

# Роутер на текстовые кнопки блока
def setup_anxiety_block(app: Application) -> None:
    # Главное подменю «Мне тяжело»
    app.add_handler(MessageHandler(filters.TEXT & filters.Regex(r"^Мне тяжело$"), menu_me_tiazhelo))
    # Подкатегория «Тревога»
    app.add_handler(MessageHandler(filters.TEXT & filters.Regex(r"^Тревога$"), handle_trevoha))
    app.add_handler(MessageHandler(filters.TEXT & filters.Regex(r"^Ещё по тревоге$"), handle_trevoha))
    # Назад
    app.add_handler(MessageHandler(filters.TEXT & filters.Regex(r"^Назад$"), handle_back))
